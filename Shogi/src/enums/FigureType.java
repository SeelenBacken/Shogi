package enums;

/**
 * Each instance of this enum represents a type of figure defined in the shogi
 * rules.
 * 
 */
public enum FigureType {

	/**
	 * Represents a not defined figure.
	 */
	UNDEFINED,

	/**
	 * Represents a king.
	 */
	KING,

	/**
	 * Represents a tower.
	 */
	TOWER,

	/**
	 * Represents a bishop.
	 */
	BISHOP,

	/**
	 * Represents a golden general.
	 */
	GOLDEN_GENERAL,

	/**
	 * Represents a silver general.
	 */
	SILVER_GENERAL,

	/**
	 * Represents a knight.
	 */
	KNIGHT,

	/**
	 * Represents a lance.
	 */
	LANCE,

	/**
	 * Represents a pawn.
	 */
	PAWN,

	/**
	 * Represents a dragon.
	 */
	DRAGON,

	/**
	 * Represents a horse.
	 */
	HORSE
}
